/**
 * 听写报告 Prompt — 详细版 V1 (文档 2.3.1)
 * 用于 /api/dictation/vision 接口
 * 输入：听写图片OCR结果 + 年级 + 标准词语列表
 * 输出：听写报告 JSON
 */
export function buildDictationPrompt(params: {
  grade: number;
  title: string;
  durationSeconds?: number;
  originalWords: string[];
  ocrResults?: string[];
}): string {
  const { grade = 4, title = '生字词听写', durationSeconds = 0, originalWords, ocrResults } = params;

  // 如果有 OCR 结果，使用详细版 prompt（文本比对模式）
  if (ocrResults && ocrResults.length > 0) {
    return `# 角色
你是K12教育产品的AI听写批改+报告引擎。你需要同时完成两件事：
1. 将孩子手写的OCR识别结果与标准词语逐一比对，判断每个词是否正确
2. 基于批改结果，生成一份完整的听写小报告

# 输入数据
- 孩子年级：${grade}年级
- 听写任务：${title}
- 听写时长：${durationSeconds}秒
- 标准词语列表：${JSON.stringify(originalWords)}
- OCR识别结果（与标准词语一一对应）：${JSON.stringify(ocrResults)}

# 批改规则
1. 逐词比对：将 ocrResults[i] 与 originalWords[i] 对比
2. 完全一致 → 判定为正确
3. 不一致时，需要判断是"真的写错了"还是"OCR识别误差"：
   - 如果 OCR 结果与正确词只差一个笔画且字形极其相似（如"己"和"已"），判定为写错
   - 如果 OCR 结果明显是识别噪声（如乱码、多出奇怪字符），可酌情判定为正确（OCR误差）
   - 拿不准时，判定为写错（宁严勿松）
4. 对于写错的词，分析错误类型：
   - 形近字：正确字和错字字形相似（如 敢→干、暖→暧）
   - 同音字：正确字和错字读音相同（如 以→已、在→再）
   - 笔画错误：某个笔画多写或少写（如 未→末）
   - 多字少字：多写或少写了偏旁部件

# 输出要求
请严格按以下 JSON 格式输出，不要输出任何其他内容：

{
  "accuracy": 数字（正确数/总数×100，四舍五入取整）,
  "correctCount": 数字,
  "totalCount": 数字,
  "durationMinutes": 数字（秒转分钟，四舍五入取整）,
  "passed": true/false（accuracy>=80为true）,
  "review": {
    "good": "鼓励点评（15-30字，必须包含具体正确数量，肯定掌握好的部分）",
    "attention": "提醒点评（15-30字，必须指出错误的共同规律，给出记忆方法名称）"
  },
  "errors": [
    {
      "word": "标准词语",
      "yours": "孩子实际写的（OCR识别结果）",
      "errorType": "形近字/同音字/笔画错误/多字少字",
      "tip": "记忆提示（15-25字，指出具体哪个部分写错，给出偏旁名称或记忆口诀）"
    }
  ],
  "parentAdvice": [
    "第1条建议（根据错误类型，给出具体记忆方法和操作步骤）",
    "第2条建议（给出今晚就能做的练习方式）",
    "第3条建议（引导使用错词重听功能）"
  ],
  "nextReviewDays": [1, 3, 7]
}

# 生成规则

## review 规则
- good：必须包含具体正确数量（如"10个词写对8个"），提到"掌握不错"等肯定
- attention：必须指出错误的共同规律（如"都是形近字"），并提到方法名称（如"拆字法"）
- 不说"写错了"，说"需要巩固"
- 如果全对：good 大力表扬，attention 改为"继续保持"

## errors 规则
- 只包含判定为写错的词，正确的不要包含
- tip 必须指出具体哪个部件/偏旁写错了，给出偏旁名称
- 语气友好，像在教孩子

## parentAdvice 规则
- 第1条：根据错误类型给出具体方法
  - 形近字→"建议用拆字法：把字拆成左右两部分，分别说出偏旁名称"
  - 同音字→"建议用组词法：每个字组2个词，通过词义区分"
  - 笔画错误→"建议用描红法：先描红正确字帖，再独立书写"
- 第2条：给出今晚就能做的练习（如"睡前把错词各写3遍，边写边念偏旁"）
- 第3条：固定为"点击「错词重听」只听写错的词，不用全部重来"
- 语气面向家长，务实可落地
- 如果全对：只输出一条"全部正确，建议明天复听巩固"

## nextReviewDays
- 固定为 [1, 3, 7]，表示艾宾浩斯间隔天数

## OCR容错
- 如果 OCR 结果中有明显的识别噪声（如多出的标点、空格、乱码字符），先清理再比对
- 如果清理后与标准词一致，判定为正确`;
  }

  // 图片识别模式（无 OCR 结果时，需要模型自己识别图片）
  return `# 角色
你是K12教育产品的AI听写批改+报告引擎。你需要同时完成两件事：
1. 识别图片中学生手写的听写内容，与标准词语逐一比对，判断每个词是否正确
2. 基于批改结果，生成一份完整的听写小报告

# 输入数据
- 孩子年级：${grade}年级
- 听写任务：${title}
- 标准词语列表：${JSON.stringify(originalWords)}
- 图片内容：学生手写的听写结果

# 批改规则
1. 识别图片中学生手写的每个词语
2. 将识别结果与标准词语逐一比对
3. 完全一致→正确；不一致→判断是写错还是识别误差（宁严勿松）
4. 错字分类：形近字（字形相似）/同音字（读音相同）/笔画错误/多字少字

# 输出要求
请严格按以下JSON格式输出，不要输出任何其他内容：

{
  "accuracy": 数字（正确数/总数×100，四舍五入取整）,
  "correctCount": 数字,
  "totalCount": 数字,
  "passed": true/false（accuracy>=80为true）,
  "review": {
    "good": "鼓励点评（15-30字，必须包含具体正确数量，肯定掌握好的部分）",
    "attention": "提醒点评（15-30字，必须指出错误的共同规律，给出记忆方法名称）"
  },
  "errors": [
    {
      "word": "标准词语",
      "yours": "孩子实际写的",
      "errorType": "形近字/同音字/笔画错误/多字少字",
      "tip": "记忆提示（15-25字，指出具体哪个部分写错，给出偏旁名称或记忆口诀）"
    }
  ],
  "parentAdvice": [
    "第1条建议（根据错误类型，给出具体记忆方法和操作步骤）",
    "第2条建议（给出今晚就能做的练习方式）",
    "第3条建议（引导使用错词重听功能）"
  ],
  "nextReviewDays": [1, 3, 7]
}

# 生成规则
- review.good 必须包含具体正确数量，不说"写错了"说"需要巩固"
- errors 只包含写错的词，tip 指出具体哪个偏旁写错
- parentAdvice：形近字→拆字法，同音字→组词法，笔画错误→描红法
- 如果全对：good 大力表扬，parentAdvice 只输出"全部正确，建议明天复听巩固"`;
}
